function [r_moon] = moonVector(jd)
	T = (jd - 2451545)/36525;

	L0 = 218.31617 + 481267.88088*T - 1.3972*T;
	l = 134.96292 + 477198.86753*T;
	lp = 357.52543 + 35999.04944*T;
	F = 93.27283 + 483202.01873*T;
	D = 297.85027 + 445267.11135*T;
	
	lam = L0 + (22640/3600)*sind(l) + (769/3600)*sind(2*l) - ...
		(4586/3600)*sind(l - 2*D) + (2370/3600)*sind(2*D) - ...
		(668/3600)*sind(lp) - (412/3600)*sind(2*F) - ...
		(212/3600)*sind(2*l - 2*D) - (206/3600)*sind(l + lp - 2*D) + ...
		(192/3600)*sind(l + 2*D) - (165/3600)*sind(lp - 2*D) + ...
		(148/3600)*sind(l - lp) - (125/3600)*sind(D) - ...
		(110/3600)*sind(l + lp) - (55/3600)*sind(2*F - 2*D);
	
	beta = (18520/3600)*sind(F + lam - L0 + (412/3600)*sind(2*F) + (541/3600)*sind(lp)) - ...
		(526/3600)*sind(F - 2*D) + (44/3600)*sind(l + F - 2*D) - ...
		(31/3600)*sind(-l + F - 2*D) - (25/3600)*sind(-2*l + F) - ...
		(23/3600)*sind(lp + F - 2*D) + (21/3600)*sind(-l + F) + ...
		(11/3600)*sind(-lp + F - 2*D);
	
	rm = 385000 - 20905*cosd(l) - 3699*cosd(2*D - l) - 2956*cosd(2*D) - ...
		570*cosd(2*l) + 246*cosd(2*l - 2*D) - 205*cosd(lp - 2*D) - 171*cosd(l + 2*D) - ...
		152*cosd(l + lp - 2*D);
	rm = rm*1000;

	eps = 23.43929111*pi/180;
	
	r_moon = R1(-eps)*rm*[cosd(lam)*cosd(beta); sind(lam)*cosd(beta); sind(beta)];
end